import gradio as gr
import pickle
import pandas as pd

# Load the trained model
model = pickle.load(open('model.pkl', 'rb'))

def predict_score(study_hours, attendance, weekly_test_score):
    # Create a DataFrame from the inputs
    input_data = pd.DataFrame([[study_hours, attendance, weekly_test_score]],
                                columns=['Study_Hours', 'Attendance_', 'Weekly_Test_Score'])

    # Make prediction
    prediction = model.predict(input_data)[0]
    return f'{prediction:.2f}'

# Create Gradio interface
iface = gr.Interface(
    fn=predict_score,
    inputs=[
        gr.Number(label='Study Hours', value=5.0),
        gr.Number(label='Attendance (%)', value=80),
        gr.Number(label='Weekly Test Score', value=70)
    ],
    outputs='text',
    title='Final Exam Score Predictor',
    description='Enter the student details to predict their final exam score.'
)

# Launch the interface
iface.launch(share=True)

